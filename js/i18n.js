let currentLang = localStorage.getItem('savoria_lang') || 'fr';

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('savoria_lang', lang);
  const t = T[lang];

  // html attributes
  document.documentElement.lang = lang === 'ar' ? 'ar' : lang === 'en' ? 'en' : 'fr';
  document.documentElement.dir  = lang === 'ar' ? 'rtl' : 'ltr';
  document.body.classList.toggle('ar', lang === 'ar');

  // text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // html (line-breaks)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key].replace(/\n/g, '<br>');
  });

  // placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // select options
  document.querySelectorAll('[data-i18n-opt]').forEach(el => {
    const key = el.getAttribute('data-i18n-opt');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // marquee
  if (document.getElementById('marqueeTrack')) buildMarquee(t.marquee_items);

  // lang btn active states
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function setLang(lang) { applyLang(lang); }

function buildMarquee(items) {
  const doubled = [...items, ...items];
  document.getElementById('marqueeTrack').innerHTML = doubled
    .map(i => `<div class="marquee-item"><span class="marquee-dot"></span><span>${i}</span></div>`)
    .join('');
}
